/**
 * Get favicon response headers
 * @returns {Object} Appropriate headers for favicon.ico responses
 */
export function getFaviconHeaders() {
  return {
    'Content-Type': 'image/x-icon',
    'Cache-Control': 'public, max-age=14400, must-revalidate',
    'X-Content-Type-Options': 'nosniff'
  };
} 